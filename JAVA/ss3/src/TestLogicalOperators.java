public class TestLogicalOperators {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int first = 10;
        int second = 20;
// Use of logical operator
        System.out.println(( first == 30) && (second == 20));
        System.out.println((first == 30) || (second == 20));
    }
}
